ch=input()
if(ch=='V'or ch=='v'):
    print("Violet")
elif(ch=='I'or ch=='i'):
    print("Indigo")
elif(ch=='B'or ch=='b'):
    print("Blue")
elif(ch=='G'or ch=='g'):
    print("Green")
elif(ch=='Y'or ch=='y'):
    print("Yellow")
elif(ch=='O'or ch=='o'):
    print("Orange")
elif(ch=='R'or ch=='r'):
    print("Red")
else:
    print("-1")